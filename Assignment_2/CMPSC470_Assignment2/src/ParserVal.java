public class ParserVal
{
    public int ival;
    public double dval;
    public String sval;
    public Object obj;

    public ParserVal()
    {
    }
    public ParserVal(int val)
    {
      ival=val;
    }
    public ParserVal(double val)
    {
      dval=val;
    }
    public ParserVal(String val)
    {
      sval=val;
    }
    public ParserVal(Object val)
    {
      obj=val;
    }
}
